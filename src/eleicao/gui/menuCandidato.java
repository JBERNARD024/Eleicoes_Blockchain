    private void listCandidatosValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listCandidatosValueChanged
        // TODO add your handling code here:
        index = listCandidatos.getSelectedIndex();
        tamanho = dList.size();
        lblRegisto.setText("Registo " + (index + 1) + "/" + tamanho);
        if (index >= 0) {
            mostraPanelCandidato();
            Candidato c = (Candidato) listCandidatos.getSelectedValues()[0];
            menu.candidatos.get(index);
            txtNome.setText(c.getNome());
            txtID.setText(String.valueOf(c.getIdCandidato()));
            txtSigla.setText(c.getSigla());
            ImageIcon icon = new ImageIcon(c.getImagem());
            Image imagem = icon.getImage().getScaledInstance(labelFoto.getWidth(), labelFoto.getHeight(), Image.SCALE_SMOOTH);
            panelFoto.setBackground(Color.white);
            labelFoto.setBackground(Color.white);
            labelFoto.setIcon(new ImageIcon(imagem));
        }
        btnPrimeiro.setEnabled(index > 1);
        btnAnterior.setEnabled(index > 1);
        btnAvancar.setEnabled(index < dList.getSize() - 1);
        btnUltimo.setEnabled(index < dList.getSize() - 1);
    }//GEN-LAST:event_listCandidatosValueChanged

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser("../Eleicao_Blockchain_Teste/candidatos");
        if (fc.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
            try {

                f = fc.getSelectedFile();
                if (!f.getName().endsWith(".cand")) {
                    f = new File(f.getAbsolutePath() + ".cand");
                }
                Recursos.writeObject(menu.candidatos, f.getAbsolutePath());
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

