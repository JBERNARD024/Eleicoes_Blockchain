    private void btnEliminaCandidatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminaCandidatoActionPerformed
        tamanho = dList.size();
        index = listCandidatos.getSelectedIndex();
        dList = Recursos.listaCandidatos(menu.candidatos);
        if (tamanho >= 0) {
            dList.remove(index);
            menu.candidatos.remove(index);
            int indice = 0;
            if (index - 1 > 0) {
                index -= 1;
                indice = index;
            } else {
                index = 0;
                indice = index;
            }
            dList.clear();
            listCandidatos.setModel(dList);
            dList = Recursos.listaCandidatos(menu.candidatos);
            listCandidatos.setModel(dList);
            listCandidatos.setSelectedIndex(indice);
        }
        btnEliminaCandidato.setEnabled(dList.size() > 0);
        panelCandidato.setVisible(dList.size() > 0);
        panelButoes.setVisible(dList.size() > 0);
        btnPrimeiro.setEnabled(index > 1);
        btnAnterior.setEnabled(index > 1);
        btnAvancar.setEnabled(index < dList.getSize() - 1);
        btnUltimo.setEnabled(index < dList.getSize() - 1);
    }//GEN-LAST:event_btnEliminaCandidatoActionPerformed

